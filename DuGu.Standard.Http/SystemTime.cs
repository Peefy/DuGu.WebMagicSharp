namespace DuGu.Standard.Http
{
    /// <summary>
    /// 系统时间结构体
    /// </summary>
    public struct SystemTime
    {
        /// <summary>
        /// 年
        /// </summary>
        public ushort wYear;
        /// <summary>
        /// 月
        /// </summary>
        public ushort wMonth;
        /// <summary>
        /// 周
        /// </summary>
        public ushort wDayOfWeek;
        /// <summary>
        /// 日
        /// </summary>
        public ushort wDay;
        /// <summary>
        /// 时
        /// </summary>
        public ushort wHour;
        /// <summary>
        /// 分
        /// </summary>
        public ushort wMinute;
        /// <summary>
        /// 秒
        /// </summary>
        public ushort wSecond;
        /// <summary>
        /// 毫秒
        /// </summary>
        public ushort wMiliseconds;
    }

}
